hook.Add("Think", "GMCustomKey", function()
	if gui.IsGameUIVisible() then return end
	for k,v in pairs(KEY_LIST) do
		if input.IsKeyDown(k) then
			if v.KeyPress == false then
				v.KeyPress = true
				v.KeyDown = false
				GAMEMODE:CustomKeyPress(k)
				hook.Run("CustomKeyPress", k)
			end
		else
			if v.KeyDown == false then
				v.KeyPress = false
				v.KeyDown = true
				GAMEMODE:CustomKeyDown(k)
				hook.Run("CustomKeyDown", k)
			end
		end
	end
end)

